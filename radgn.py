def _trim_start(df, mae_col='mean_val_mae', factor=4.0):
    """
    Return index of first row where mean_val_mae < factor * final_mean.
    'final_mean' = avg of last 10% of rows (or last 5 rows).
    Returns 0 if no trimming needed.
    """
    vals = np.array(df[mae_col].values, dtype=float)
    n    = len(vals)
    tail = max(5, n // 10)
    final_mean = np.nanmean(vals[-tail:])
    if np.isnan(final_mean) or final_mean <= 0:
        return 0
    threshold = factor * final_mean
    for i, v in enumerate(vals):
        if not np.isnan(v) and v < threshold:
            return i
    return 0

