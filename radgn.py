def _export_epoch_hist(hist, mname, base_name):
    _kv = next((k for k in ('val_mae','val','val_loss')
                if k in hist[0] and isinstance(hist[0][k], (list,tuple,np.ndarray))), None)
    _kt = next((k for k in ('train_mae','train','train_loss')
                if k in hist[0] and isinstance(hist[0][k], (list,tuple,np.ndarray))), None)
    if _kv is None:
        print(f'  SKIP {mname}: val_mae not a list (XGB scalar hist)')
        return
    _max_ep = max(len(_h.get(_kv,[])) for _h in hist)
    if _max_ep == 0: return

    _df = pd.DataFrame({'epoch': range(1, _max_ep+1)})
    _vv, _tv = [], []
    for _fi, _h in enumerate(hist):
        _vm = list(_h.get(_kv,[])); _vm_p = _vm + [np.nan]*(_max_ep-len(_vm))
        _df[f'fold{_fi+1}_val_mae'] = _vm_p; _vv.append(_vm_p)
        if _kt:
            _tm = list(_h.get(_kt,[])); _tm_p = _tm + [np.nan]*(_max_ep-len(_tm))
            _df[f'fold{_fi+1}_train_mae'] = _tm_p; _tv.append(_tm_p)
    _arr_v = np.array(_vv, dtype=float)
    _df['mean_val_mae'] = np.nanmean(_arr_v, axis=0)
    _df['std_val_mae']  = np.nanstd(_arr_v,  axis=0)
    if _tv:
        _arr_t = np.array(_tv, dtype=float)
        _df['mean_train_mae'] = np.nanmean(_arr_t, axis=0)
        _df['std_train_mae']  = np.nanstd(_arr_t,  axis=0)

    _sv_curve(_df, base_name,
              f'X=epoch  Y=fold1-5_val+mean+std  ({mname})')

