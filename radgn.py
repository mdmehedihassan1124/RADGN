    opt_f = torch.optim.AdamW(mf.parameters(), lr=CFG.gnn_lr, weight_decay=CFG.gnn_wd)
    sched_f = make_plateau_sched(opt_f)
    bf, ckpt_f, pat_f = np.inf, None, 0
    for ep in range(CFG.gnn_epochs):
        if ep < CFG.warmup_epochs:
            for pg in opt_f.param_groups:
                pg['lr'] = CFG.gnn_lr * warmup_factor(ep)
        gnn_train_epoch(mf, tr_f, opt_f, crit, DEVICE, ep)
        vp_hv, _ = gnn_eval(mf, vl_f, DEVICE)
        vr_hv = mean_absolute_error(y_hv, vp_hv)
        if ep >= CFG.warmup_epochs:
            sched_f.step(vr_hv)
        if vr_hv < bf:
            bf = vr_hv
            ckpt_f = save_checkpoint(mf.state_dict(), f'{model_name}_final')
            pat_f = 0
        else:
            pat_f += 1
        if pat_f >= CFG.gnn_patience:
            break

    mf.load_state_dict(load_checkpoint(ckpt_f))
    tp, tt = gnn_eval(mf, te_f, DEVICE)
    mae_te, rmse_te, r2_te, rho_te = eval_metrics(tt, tp)
    elapsed = time.time() - t0
    log.info(f'{model_name} Test: MAE={mae_te:.3f} RMSE={rmse_te:.3f} '
             f'R²={r2_te:.4f} ρ={rho_te:.3f} t={elapsed:.0f}s')

    # Memory cleanup (fix C13)
    if torch.cuda.is_available():
        torch.cuda.empty_cache()

    return cv, history, tp, mae_te, rmse_te, r2_te, mf, elapsed

