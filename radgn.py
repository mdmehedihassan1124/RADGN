def eval_metrics(y_true, y_pred):
    '''Compute MAE, RMSE, R², Spearman ρ (fix E4: Spearman instead of Pearson).'''
    mae = mean_absolute_error(y_true, y_pred)
    rmse = float(np.sqrt(mean_squared_error(y_true, y_pred)))
    r2 = r2_score(y_true, y_pred)
    rho = spearmanr(y_true, y_pred)[0]  # Fix E4: rank correlation
    return mae, rmse, r2, rho

def bootstrap_ci(y_true, y_pred, metric_fn, n_boot=2000, ci=0.95, seed=42):
    '''Compute bootstrap confidence interval for a metric (fix Q1).'''
    rng = np.random.RandomState(seed)
    n = len(y_true)
    vals = []
    for _ in range(n_boot):
        idx = rng.choice(n, n, replace=True)
        vals.append(metric_fn(y_true[idx], y_pred[idx]))
    alpha = (1 - ci) / 2
    lo, hi = np.percentile(vals, [100*alpha, 100*(1-alpha)])
    return np.mean(vals), lo, hi

