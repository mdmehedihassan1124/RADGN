def get_scaffold(smi):
    '''Compute Murcko scaffold with proper error handling (fix C4).'''
    mol = Chem.MolFromSmiles(smi)
    if mol is None:
        log.warning(f'Cannot parse SMILES for scaffold: {smi[:50]}...')
        return smi
    try:
        sc = MurckoScaffold.MurckoScaffoldSmiles(mol=mol)
        return sc if sc else smi
    except ValueError as e:
        log.warning(f'Scaffold computation failed for {smi[:50]}: {e}')
        return smi

