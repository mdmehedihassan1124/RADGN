# ── Statistical Significance Tests — Bootstrap Permutation + Paired t-test ─────
# PROPOSED MODELS: RADGN (★), Hybrid-RADGN (★), D-MPNN+XGB (†)
# TEST: one-tailed (H₁: Hybrid-RADGN MAE < baseline MAE)
# CORRECTION: Holm-Bonferroni across all comparisons
from scipy.stats import ttest_rel

n_test_stat = len(y_te_g)
ae_hyb = np.abs(y_te_g - pred_hybrid_te)
baselines_stat = [m for m in ALL_MODELS if m != 'Hybrid-RADGN']
n_comp = len(baselines_stat)

def bootstrap_perm_one_tailed(ae1, ae2, n_perm=10000, seed=42):
    """One-tailed bootstrap permutation: P(obs_diff >= |null_diff|).
    H₁: mean(ae1) < mean(ae2) → proposed model is better."""
    rng = np.random.RandomState(seed)
    obs_diff = np.mean(ae1) - np.mean(ae2)   # negative if ae1 better
    combined = np.column_stack([ae1, ae2])
    count = 0
    for _ in range(n_perm):
        swap = rng.random(len(ae1)) > 0.5
        pa = np.where(swap, combined[:,1], combined[:,0])
        pb = np.where(swap, combined[:,0], combined[:,1])
        perm_diff = np.mean(pa) - np.mean(pb)
        # One-tailed: count nulls as extreme as obs in the direction of obs_diff
        if obs_diff < 0:
            if perm_diff <= obs_diff: count += 1
        else:
            if perm_diff >= obs_diff: count += 1
    return count / n_perm

def glass_delta(ae1, ae2):
    """Glass's Δ: standardised by baseline SD (fix S5)."""
    return (np.mean(ae2) - np.mean(ae1)) / (np.std(ae2, ddof=1) + 1e-9)

log.info(f'n_test={n_test_stat} | {n_comp} comparisons | one-tailed | Holm-Bonferroni')
stat_results = []
raw_pvals    = []

for bsl in baselines_stat:
    ae_b = np.abs(y_te_g - all_preds_te[bsl])
    diff = ae_b - ae_hyb      # positive = Hybrid better

    # Bootstrap permutation (one-tailed)
    p_perm = bootstrap_perm_one_tailed(ae_hyb, ae_b)

    # Paired t-test (one-tailed: Hybrid < baseline)
    t_stat, p_two = ttest_rel(ae_hyb, ae_b)
    p_ttest = p_two / 2 if t_stat < 0 else 1 - p_two/2   # one-tailed

    g_delta = glass_delta(ae_hyb, ae_b)
    raw_pvals.append(p_perm)
    stat_results.append({
        'Baseline': bsl,
        'n_test': n_test_stat,
        'ΔMAE_mean': float(diff.mean()),
        'ΔMAE_median': float(np.median(diff)),
        'p_perm (1-tail)': p_perm,
        't_stat': float(t_stat),
        'p_ttest (1-tail)': p_ttest,
        'Glass_Δ': g_delta,
    })
    log.info(f'  vs {bsl:22s}: ΔMAE={diff.mean():+.3f} | p_perm={p_perm:.4f} | t={t_stat:.3f} | p_t={p_ttest:.4f}')

# Holm-Bonferroni correction on permutation p-values
sorted_idx = np.argsort(raw_pvals)
corrected  = np.ones(n_comp)
for rank, idx in enumerate(sorted_idx):
    corrected[idx] = min(raw_pvals[idx] * (n_comp - rank), 1.0)

for i, res in enumerate(stat_results):
    res['p_perm_HB'] = float(corrected[i])
    sig = '***' if corrected[i]<0.001 else '**' if corrected[i]<0.01 else '*' if corrected[i]<0.05 else 'ns'
    res['Sig (HB)'] = sig

stat_df = pd.DataFrame(stat_results)
print(f'\nTable 4. Statistical Tests: Hybrid-RADGN vs All Baselines')
print(f'(One-tailed: H₁ = Hybrid-RADGN MAE < baseline; n_test={n_test_stat})')
