def quick_hybrid_cv(embs, X_delta, y, folds, feature_set='emb_delta', head='xgb', seed=SEED):
    maes = []
    for ti, vi in folds:
        if feature_set == 'emb_only':
            sc_ = StandardScaler(); Xtr = sc_.fit_transform(embs[ti]); Xvl = sc_.transform(embs[vi])
        elif feature_set == 'delta_only':
            Xtr, Xvl = X_delta[ti], X_delta[vi]
        elif feature_set == 'pca_delta':
            sc_ = StandardScaler(); e_sc = sc_.fit_transform(embs[ti]); e_vl = sc_.transform(embs[vi])
            pca_ = _PCA(n_components=min(64, e_sc.shape[1]), whiten=True, random_state=seed)
            Xtr = np.hstack([pca_.fit_transform(e_sc), X_delta[ti]])
            Xvl = np.hstack([pca_.transform(e_vl), X_delta[vi]])
        else:  # emb_delta (default)
            sc_ = StandardScaler()
            Xtr = np.hstack([sc_.fit_transform(embs[ti]), X_delta[ti]])
            Xvl = np.hstack([sc_.transform(embs[vi]), X_delta[vi]])
        if head == 'xgb':
            _m = xgb.XGBRegressor(
                n_estimators=600, max_depth=5, learning_rate=0.03, subsample=0.8,
                colsample_bytree=0.8, min_child_weight=3, reg_alpha=0.1, reg_lambda=1.5,
                n_jobs=-1, random_state=seed, tree_method='hist', device=_XGB_DEVICE,
                eval_metric='mae', early_stopping_rounds=60)
            _m.fit(Xtr, y[ti], eval_set=[(Xvl, y[vi])], verbose=False); pv = _m.predict(Xvl)
        elif head == 'ridge':
            from sklearn.linear_model import Ridge as _R
            sc2 = StandardScaler(); _m = _R(alpha=10.0)
            _m.fit(sc2.fit_transform(Xtr), y[ti]); pv = _m.predict(sc2.transform(Xvl))
        else:
            sc2 = StandardScaler()
            Xt = torch.tensor(sc2.fit_transform(Xtr), dtype=torch.float32).to(DEVICE)
            yt = torch.tensor(y[ti], dtype=torch.float32).to(DEVICE)
            Xv = torch.tensor(sc2.transform(Xvl), dtype=torch.float32).to(DEVICE)
            mlp = torch.nn.Sequential(
                torch.nn.Linear(Xtr.shape[1], 256), torch.nn.ELU(),
                torch.nn.Dropout(0.25), torch.nn.Linear(256, 128), torch.nn.ELU(),
                torch.nn.Linear(128, 1)).to(DEVICE)
            opt_ = torch.optim.AdamW(mlp.parameters(), lr=5e-4)
            cr_  = torch.nn.HuberLoss(delta=3.0)
            for _ in range(200):
                mlp.train(); opt_.zero_grad()
                cr_(mlp(Xt).squeeze(-1), yt).backward(); opt_.step()
            mlp.eval()
            with torch.no_grad(): pv = mlp(Xv).squeeze(-1).cpu().numpy()
            maes.append(mean_absolute_error(y[vi], pv)); continue
        maes.append(mean_absolute_error(y[vi], pv))
    return float(np.mean(maes)), float(np.std(maes))

