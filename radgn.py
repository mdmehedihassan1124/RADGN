        super().__init__()
        self.conv1 = GATConv(node_dim, hidden, heads=heads, dropout=drop)
        self.conv2 = GATConv(hidden * heads, embed, heads=1, concat=False, dropout=drop)
        self.bn1 = nn.BatchNorm1d(hidden * heads)
        self.bn2 = nn.BatchNorm1d(embed)
        self.drop = nn.Dropout(drop)
