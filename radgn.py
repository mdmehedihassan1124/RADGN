# ── RADGN Training & Evaluation Functions ──────────────────────────────────
def radgn_train_ep(model, loader, opt, crit, dev, ep=0):
    """Train one epoch of RADGN model."""
    model.train()
    total = 0
    for du, ds in loader:
        du, ds = du.to(dev), ds.to(dev)
        opt.zero_grad()
        pred, _ = model(du, ds)
        y_tgt = du.y.squeeze()
        if CFG.label_noise_std > 0:
            y_tgt = y_tgt + torch.randn_like(y_tgt) * CFG.label_noise_std
        loss = crit(pred, y_tgt)
        loss.backward()
        torch.nn.utils.clip_grad_norm_(model.parameters(), CFG.grad_clip)
        opt.step()
        total += loss.item() * du.num_graphs
    return total / len(loader.dataset)

@torch.no_grad()
def radgn_eval(model, loader, dev):
    """Evaluate RADGN model on a loader."""
    model.eval()
    preds, trues, embs = [], [], []
    for du, ds in loader:
        du, ds = du.to(dev), ds.to(dev)
        pred, rxn = model(du, ds)
        preds.extend(pred.cpu().tolist())
        trues.extend(du.y.squeeze().cpu().tolist())
        embs.append(rxn.cpu().numpy())
    return np.array(preds), np.array(trues), np.vstack(embs)

def radgn_embed(model, loader, dev):
    """Extract only embeddings from RADGN model (wrapper around radgn_eval)."""
    _, trues, embs = radgn_eval(model, loader, dev)
    return embs, trues

# ── Generic GNN Embedding Extraction ─────────────────────────────────────────
@torch.no_grad()
def gnn_embed(model, loader, dev):
    """Extract embeddings from any GNN model (single graphs, not paired)."""
    model.eval()
    embs, trues = [], []
    for batch in loader:
        # Handle both single Data objects and Batch objects
        if isinstance(batch, Batch):
            batch = batch.to(dev)
        else:
            batch = batch.to(dev)
        _, emb = model(batch)
        embs.append(emb.cpu().numpy())
        trues.extend(batch.y.squeeze().cpu().tolist())
    return np.vstack(embs), np.array(trues)

