# -- RADGN v5: 3-layer edge-aware GAT, LayerNorm, residuals, attention pooling --
# Key v5 fixes vs v4:
#   1. edge_dim=4  — bond features NOW used in all GAT layers (was ignored in v4)
#   2. 3 layers + residual projections (was 2 layers, no residuals)
#   3. LayerNorm (not BatchNorm — stable for shared encoder called twice per fwd)
#   4. xavier_uniform_(gain=sqrt(2)) init for ELU activations
#   5. Attention pooling (learned per-atom importance vs simple mean pool)
#   6. LayerNorm on rxn embedding before MLP head
#   7. Configurable combinator: 2-way / 3-way / 4-way (ablation-selected)

from torch_geometric.nn import GlobalAttention as _GlobalAttn

class RADGN(nn.Module):
    def __init__(self, node_dim=14, edge_dim=4, hidden=128, heads=4,
                 embed=96, drop=0.20, combine_mode='4way'):
        super().__init__()
        self.embed        = embed
        self.combine_mode = combine_mode
        # 3-layer edge-aware GAT
        self.conv1 = GATConv(node_dim,     hidden, heads=heads, dropout=drop,
                             edge_dim=edge_dim, add_self_loops=False, concat=True)
        self.conv2 = GATConv(hidden*heads, hidden, heads=heads, dropout=drop,
                             edge_dim=edge_dim, add_self_loops=False, concat=True)
        self.conv3 = GATConv(hidden*heads, embed,  heads=1,     dropout=drop,
                             edge_dim=edge_dim, add_self_loops=False, concat=False)
        self.ln1 = nn.LayerNorm(hidden * heads)
        self.ln2 = nn.LayerNorm(hidden * heads)
        self.ln3 = nn.LayerNorm(embed)
        self.proj_in   = nn.Linear(node_dim,       hidden * heads, bias=False)
        self.proj_res2 = nn.Linear(hidden * heads, hidden * heads, bias=False)
        self.drop_layer = nn.Dropout(drop)
        # Learned attention pooling: better than mean pool for reaction prediction
        self.attn_pool = _GlobalAttn(
            gate_nn=nn.Sequential(
                nn.Linear(embed, embed // 2), nn.Tanh(),
                nn.Linear(embed // 2, 1)
            )
        )
        self.ln_pool = nn.LayerNorm(embed)
        # Reaction combinator dimension
        _rxn_d = {'2way': 2*embed, '3way': 3*embed, '4way': 4*embed}
        self.rxn_d    = _rxn_d.get(combine_mode, 4*embed)
        self.rxn_norm = nn.LayerNorm(self.rxn_d)
        self.rxn_fc   = nn.Sequential(
            nn.Linear(self.rxn_d,   self.rxn_d), nn.ELU(), nn.Dropout(drop),
            nn.Linear(self.rxn_d,   2*embed),    nn.ELU(), nn.Dropout(drop*0.5),
            nn.Linear(2*embed,      embed),       nn.ELU(),
            nn.Linear(embed,        1),
        )
        self.rxn_skip = nn.Linear(self.rxn_d, 1, bias=False)
        # xavier_uniform_(gain=sqrt(2)) — correct init for ELU activations
        for m in self.modules():
            if isinstance(m, nn.Linear):
                nn.init.xavier_uniform_(m.weight, gain=1.4142)
                if m.bias is not None: nn.init.zeros_(m.bias)

    def encode(self, x, ei, ea, batch):
        x1 = F.elu(self.ln1(self.conv1(x,  ei, ea))) + self.proj_in(x)
        x1 = self.drop_layer(x1)
        x2 = F.elu(self.ln2(self.conv2(x1, ei, ea))) + self.proj_res2(x1)
        x2 = self.drop_layer(x2)
        x3 = F.elu(self.ln3(self.conv3(x2, ei, ea)))
        return self.ln_pool(self.attn_pool(x3, batch))   # attention pooling

    def rxn_embed(self, hu, hs):
        if   self.combine_mode == '2way': return torch.cat([hu, hs], dim=1)
        elif self.combine_mode == '3way': return torch.cat([hu, hs, hu-hs], dim=1)
        else:                             return torch.cat([hu, hs, hu-hs, hu*hs], dim=1)

    def forward(self, du, ds):
        ea_u = getattr(du, 'edge_attr', None)
        ea_s = getattr(ds, 'edge_attr', None)
        hu   = self.encode(du.x, du.edge_index, ea_u, du.batch)
        hs   = self.encode(ds.x, ds.edge_index, ea_s, ds.batch)
        rxn  = self.rxn_embed(hu, hs)
        rxn_n = self.rxn_norm(rxn)
        return self.rxn_fc(rxn_n).squeeze(-1) + self.rxn_skip(rxn_n).squeeze(-1), rxn


class LOHCAwareHuberLoss(nn.Module):
    def __init__(self, delta=3.0, lohc_lo=40.0, lohc_hi=70.0, lohc_weight=2.5):
        super().__init__()
        self.delta=delta; self.lo=lohc_lo; self.hi=lohc_hi; self.w=lohc_weight
    def forward(self, pred, target):
        diff  = pred - target
        huber = torch.where(diff.abs()<self.delta, 0.5*diff**2,
                            self.delta*(diff.abs()-0.5*self.delta))
        mask  = (target>=self.lo) & (target<=self.hi)
        wt    = torch.where(mask, torch.full_like(target,self.w), torch.ones_like(target))
        return (huber*wt).mean()

class QuantileReg(nn.Module):
    def __init__(self, base_loss, q=0.1, alpha=0.04):
        super().__init__()
        self.base=base_loss; self.q=q; self.alpha=alpha
    def forward(self, pred, target):
        diff = target - pred
        return self.base(pred,target) + self.alpha*torch.where(diff>=0,self.q*diff,(self.q-1)*diff).mean()

