class DMPNN(nn.Module):
    def __init__(self, node_dim=14, edge_dim=4, hidden=192, embed=96,
                 depth=2, drop=0.25):
        super().__init__()
        self.depth = depth
        self.drop = nn.Dropout(drop)
        self.edge_init = nn.Linear(node_dim + edge_dim, hidden)
        self.edge_upd = nn.ModuleList([nn.Linear(hidden, hidden) for _ in range(depth)])
        self.bns = nn.ModuleList([nn.BatchNorm1d(hidden) for _ in range(depth)])
        self.node_proj = nn.Linear(node_dim + hidden, embed)
        self.bn_out = nn.BatchNorm1d(embed)
        self.fc = nn.Sequential(nn.Linear(embed, 48), nn.ELU(),
                                nn.Dropout(drop), nn.Linear(48, 1))
    def forward(self, data):
        x, ei, ea, batch = data.x, data.edge_index, data.edge_attr, data.batch
        n, ne = x.size(0), ei.size(1)
        if ne == 0:
            emb = global_mean_pool(
                self.drop(F.relu(self.bn_out(self.node_proj(
                    torch.cat([x, x.new_zeros(n, self.node_proj.in_features - x.size(1))], dim=-1)
                )))), batch)
            return self.fc(emb).squeeze(-1), emb
        if ea is None or ea.size(0) == 0:
            ea = x.new_zeros(ne, 4)
        src, dst = ei[0], ei[1]
        h = F.relu(self.edge_init(torch.cat([x[src], ea], dim=-1)))
        for upd, bn in zip(self.edge_upd, self.bns):
            agg = x.new_zeros(n, h.size(-1))
            agg.scatter_add_(0, dst.unsqueeze(-1).expand_as(h), h)
            h = self.drop(F.relu(bn(upd(agg[src] - h))))
        atom_h = x.new_zeros(n, h.size(-1))
        atom_h.scatter_add_(0, dst.unsqueeze(-1).expand_as(h), h)
        emb = self.drop(F.relu(self.bn_out(
            self.node_proj(torch.cat([x, atom_h], dim=-1)))))
        emb = global_mean_pool(emb, batch)
        return self.fc(emb).squeeze(-1), emb

class AttentiveFP(nn.Module):
    def __init__(self, node_dim=14, hidden=96, heads=4, embed=64,
                 num_layers=2, drop=0.25):
        super().__init__()
        self.drop = nn.Dropout(drop)
        self.node_emb = nn.Linear(node_dim, hidden)
        self.convs = nn.ModuleList([
            GATv2Conv(hidden, hidden // heads, heads=heads, dropout=drop, concat=True)
            for _ in range(num_layers)
        ])
        self.bns = nn.ModuleList([nn.BatchNorm1d(hidden) for _ in range(num_layers)])
        self.readout = nn.Linear(hidden, 1)
        self.proj = nn.Sequential(nn.Linear(hidden, embed), nn.ELU(), nn.Dropout(drop))
        self.fc = nn.Sequential(nn.Linear(embed, 48), nn.ELU(),
                                nn.Dropout(drop), nn.Linear(48, 1))
    def forward(self, data):
        x, ei, batch = data.x, data.edge_index, data.batch
        h = self.drop(F.elu(self.node_emb(x)))
        for conv, bn in zip(self.convs, self.bns):
            h = self.drop(F.elu(bn(conv(h, ei))))
        score = self.readout(h)
        se = torch.exp(score - score.max())
        denom = x.new_zeros(batch.max().item() + 1, 1)
        denom.scatter_add_(0, batch.unsqueeze(-1), se)
        aw = se / (denom[batch] + 1e-9)
        hw = aw * h
        ge = x.new_zeros(batch.max().item() + 1, h.size(-1))
        ge.scatter_add_(0, batch.unsqueeze(-1).expand_as(hw), hw)
        emb = self.proj(ge)
        return self.fc(emb).squeeze(-1), emb

